// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(min1, min2) {
  if (min1 < min2){
    return min1;
    }else {
      return min2;
    }
  
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(number) {
  if (number % 2 == 0){
    return true;
  }else {
    return false;
  }
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(str, c) {
  var cnt = 0;
  for(var i = 0; i < str.length; i++) {
      if (str.charAt(i) === c) { 
          cnt++;
      }
  }
  return cnt;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(str) {
  var cnt = 0;
  for(var i = 0; i < str.length; i++) {
      if (str.charAt(i) === "B") { 
          cnt++;
      }
  }
  return cnt;
}

